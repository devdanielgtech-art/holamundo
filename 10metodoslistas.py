# Metdos de listas
numeros = [1,2,3,4,5]

# Adicionar elementos a una lista
numeros.append(6)
print(numeros)

# Insertar ELEMEMTOS en una posision determinada
numeros.insert(0,-1)
print(numeros)

numeros.insert(1,0)
print(numeros)

# Elimina un elemento en su primera aparicion
numeros.remove(1)
print(numeros)

# Verificar si un elemento se encuentra en la lista
print(4 in numeros)

# Tamanño de la lista
print(len(numeros))

# Elimina el contenido de la lista
numeros.clear()

